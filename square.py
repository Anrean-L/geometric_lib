
def area(a):
    '''
    Returns the area of a square on its side

        Parameters:
            a (float): square side length

        Return value:
            square_area (float): square area
    '''
    return a * a


def perimeter(a):
    '''
    Returns the perimeter of the square on its side

        Parameters:
            a (float): square side length

        Return value:
            square_perimeter (float): square perimeter
    '''
    return 4 * a
